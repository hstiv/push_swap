#include <unistd.h>
#include "libft.h"

void				ft_putnbr(int n)
{
	if (!n || (n > 2147483647 && n <= -2147483648))
		n = 0;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n = n * (-1);
		ft_putchar('-');
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
}
